CREATE TABLE TYPE_OF_FOOD(
ID INT PRIMARY KEY,
FOOD_TYPE VARCHAR(20) UNIQUE KEY,
TIME_DURATION_FROM TIME,
TIMEDURATIONTO TIME
)

CREATE TABLE MENU_ITEM(
ID INT PRIMARY KEY,
ITEMS VARCHAR(20),
RATE INT
)

CREATE TABLE ORDER_RECORDS(
ID INT PRIMARY KEY AUTO_INCREMENT,
ORDER_ID INT,
SEAT_NUMBER INT,
FOOD_ORDERED VARCHAR(20),
QUANTITY INT,
TOTAL INT,
ORDER_DATE DATE,
STATUS VARCHAR(20)
)

CREATE TABLE SEAT(
ID INT PRIMARY KEY,
SEAT_NO INT,
SEAT_STATUS VARCHAR(30)
)

CREATE TABLE SCHEDULES(
ID INT PRIMARY KEY AUTO_INCREMENT,
FOOD_TYPE INT,
MENU_TYPE INT,
QUANTITY INT,
CONSTRAINT FoodType_fk1 FOREIGN KEY(FOOD_TYPE)REFERENCES TYPE_OF_FOOD(ID),
CONSTRAINT MenuType_fk2 FOREIGN KEY(MENU_TYPE)REFERENCES MENU_ITEM(ID)
) 


CREATE TABLE ORDER_GIVEN(
ID INT PRIMARY KEY AUTO_INCREMENT,
ITEM VARCHAR(20),
QUANTITY INT
)

CREATE TABLE BILLING(
ID INT PRIMARY KEY AUTO_INCREMENT,
SEAT_NUMBER INT,
STATUS VARCHAR(20)
)


